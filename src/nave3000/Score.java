
package nave3000;

public class Score
{
    private int score;
    private String name;
    
    public void setScore (int score)
    {
        this.score = score;
    }
    
    public int getScore ()
    {
        return this.score;
    }
    
    public void setName (String name)
    {
        this.name = name;
    }
    
    public String getName ()
    {
        return this.name;
    }
}
